#include "GameState_Enter.h"
#include "GameState_Play.h"
#include "Scene_Game.h"


////////////////////////////////////////////////////////////////////////////////////////
//
// コンストラクタ：デストラクタ
//
////////////////////////////////////////////////////////////////////////////////////////
GameState_Enter::GameState_Enter(Scene_Base* pScene) : State_Base(pScene){
    pGameScene_ = dynamic_cast<Scene_Game*>(pScene);
    stageSelector_ = std::make_unique<StageSelector>();
    stageSelector_->SetStageManager(pGameScene_->Get_pStageManager());
    stageSelector_->SetCamera(pGameScene_->Get_pCamera());
}

GameState_Enter::~GameState_Enter(){}

////////////////////////////////////////////////////////////////////////////////////////
//
// 初期化処理
//
////////////////////////////////////////////////////////////////////////////////////////
void GameState_Enter::Initialize(){}

////////////////////////////////////////////////////////////////////////////////////////
//
// 終了処理
//
////////////////////////////////////////////////////////////////////////////////////////
void GameState_Enter::Finalize(){}

////////////////////////////////////////////////////////////////////////////////////////
//
// 更新処理
//
////////////////////////////////////////////////////////////////////////////////////////
void GameState_Enter::Update(){
    stageSelector_->Update();
}

////////////////////////////////////////////////////////////////////////////////////////
//
// 描画処理
//
////////////////////////////////////////////////////////////////////////////////////////
void GameState_Enter::Draw(){
    stageSelector_->Draw();
}

////////////////////////////////////////////////////////////////////////////////////////
//
// フレーム開始処理
//
////////////////////////////////////////////////////////////////////////////////////////
void GameState_Enter::BeginFrame(){}

////////////////////////////////////////////////////////////////////////////////////////
//
// フレーム終了処理
//
////////////////////////////////////////////////////////////////////////////////////////
void GameState_Enter::EndFrame(){}

////////////////////////////////////////////////////////////////////////////////////////
//
// コライダーをCollisionManagerに渡す
//
////////////////////////////////////////////////////////////////////////////////////////
void GameState_Enter::HandOverColliders(){}


////////////////////////////////////////////////////////////////////////////////////////
//
// ステート管理
//
////////////////////////////////////////////////////////////////////////////////////////
void GameState_Enter::ManageState(){

    // ステージが決定されたらプレイステートに遷移
    if(stageSelector_->GetIsDecided()){
        pGameScene_->ChangeState(new GameState_Play(pGameScene_));
    }
}
