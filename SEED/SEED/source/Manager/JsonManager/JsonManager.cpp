JsonManager* JsonManager::instance_ = nullptr;


///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/*                                                                                                               */
/*                                                初期化、終了処理                                                  */
/*                                                                                                               */
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////

JsonManager* JsonManager::GetInstance(){
    if(instance_ == nullptr){
        instance_ = new JsonManager();
    }
    return instance_;

}

void JsonManager::Initialize(){
    // JsonManagerの初期化
    GetInstance();

}

