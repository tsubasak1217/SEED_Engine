#include "FieldObject_ViewPoint.h"

////////////////////////////////////////////////////////////////////////
// コンストラクタ
////////////////////////////////////////////////////////////////////////
FieldObject_ViewPoint::FieldObject_ViewPoint(){
    className_ = "FieldObject_ViewPoint";
    name_ = "viewPoint";
    // モデルの初期化
    std::string path = "FieldObject/" + name_ + ".obj";
    model_ = std::make_unique<Model>("Assets/Cube.obj");
    model_->isRotateWithQuaternion_ = false;
    // コライダー関連の初期化
    colliderEditor_ = std::make_unique<ColliderEditor>(className_, this);
    InitColliders(ObjectType::Field);
    // 全般の初期化
    FieldObject::Initialize();
}

////////////////////////////////////////////////////////////////////////
// 初期化関数
////////////////////////////////////////////////////////////////////////
void FieldObject_ViewPoint::Initialize(){}


////////////////////////////////////////////////////////////////////////
// 更新関数
////////////////////////////////////////////////////////////////////////
void FieldObject_ViewPoint::Update(){
    FieldObject::Update();
}

////////////////////////////////////////////////////////////////////////
// 描画関数
////////////////////////////////////////////////////////////////////////
void FieldObject_ViewPoint::Draw(){
#ifdef _DEBUG
    FieldObject::Draw();
#endif // _DEBUG
}

////////////////////////////////////////////////////////////////////////
// 編集関数
////////////////////////////////////////////////////////////////////////
void FieldObject_ViewPoint::Edit(){
#ifdef _DEBUG
    // 基本の編集
    FieldObject::Edit();

    // 固有の編集
    ImGui::Text("ViewPoint Settings");
    ImGui::Separator();
    ImGui::DragFloat("distance", &distance_, 0.5f, 10.0f);
    ImGui::Separator();
#endif // _DEBUG
}


////////////////////////////////////////////////////////////////////////
// Json出力関数
////////////////////////////////////////////////////////////////////////
nlohmann::json FieldObject_ViewPoint::OutputJson(){
    nlohmann::json json = FieldObject::OutputJson();
    json["distance"] = distance_;
    return json;
}
